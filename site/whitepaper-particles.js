    getMirroredSquigglePositions() {
        const original = this.getRawSquigglePoints();
        const mirrored = original.map(p => ({ x: 1 - p.x, y: p.y }));
        return this.samplePositions(mirrored);
    }

    // ------------------------------------------------------------------
    // SHAPE: ARROW (procedural — shaft + head pointing right)
    // ------------------------------------------------------------------

    getArrowPositions() {
        const points = [];

        // Shaft: 5 horizontal rows, y: 0.4 to 0.6, x: 0.1 to 0.7
        const shaftRows = [0.44, 0.47, 0.50, 0.53, 0.56];
        shaftRows.forEach(y => {
            for (let x = 0.1; x <= 0.7; x += 0.05) {
                points.push({ x, y });
            }
        });

        // Head: triangle at x: 0.7 to 0.95, converging to tip at (0.95, 0.5)
        const tipX = 0.95;
        const baseX = 0.7;
        const centerY = 0.5;
        const halfHeight = 0.3; // y spans 0.2 to 0.8

        const steps = 10;
        for (let i = 0; i <= steps; i++) {
            const progress = i / steps;
            const x = baseX + (tipX - baseX) * progress;
            const spread = halfHeight * (1 - progress);

            // Top and bottom edges
            points.push({ x, y: centerY - spread });
            points.push({ x, y: centerY + spread });

            // Fill interior rows for denser triangles at the base
            if (spread > 0.03) {
                const innerSteps = Math.max(1, Math.floor(spread / 0.06));
                for (let j = 1; j < innerSteps * 2; j++) {
                    const fillY = (centerY - spread) + (2 * spread) * (j / (innerSteps * 2));
                    points.push({ x, y: fillY });
                }
            }
        }

        return this.samplePositions(points);
    }

