    // SHAPE: SQUIGGLE LEFT HALF (x < 0.35, rescaled to fill 0-1)
    // ------------------------------------------------------------------

    getSquiggleLeftPositions() {
        const all = this.getRawSquigglePoints();

        // Filter to the chaotic left portion only
        const leftPoints = all.filter(p => p.x < 0.35);
        const cropped = leftPoints.slice(0, 300);

        // Rescale x from 0.0-0.35 range to fill 0.05-0.95
        const minX = Math.min(...cropped.map(p => p.x));
        const maxX = Math.max(...cropped.map(p => p.x));
        const rangeX = maxX - minX || 1;

        const rescaled = cropped.map(p => ({
            x: 0.05 + ((p.x - minX) / rangeX) * 0.9,
            y: p.y
        }));

        return this.samplePositions(rescaled);
    }

