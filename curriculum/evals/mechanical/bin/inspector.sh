#!/usr/bin/env bash
# Inspector — Haiku-powered transcript coherence triage.
#
# Reads an Actor transcript end-to-end and flags sequence/coherence issues
# that the deterministic judge can't catch: prompt conflation, mid-chain
# drift, looping on a phase, asking off-script questions, framing leaks
# across phases.
#
# This is NOT a PASS/FAIL gate. It's advisory. The script judge already
# decides PASS/FAIL on assertions. Inspector writes notes for human review
# when the chain is long enough that a reader would benefit from a triage.
#
# Verdict ends with PASS-WITH-NOTES (clean) or REVIEW-NEEDED (auditor look).
#
# Usage:
#   inspector.sh <transcript.jsonl> <slug>
#
# Writes: instances/<slug>-inspector-notes.md
# Exit 0 always (advisory, not a gate). Failure to dispatch returns 2.

set -uo pipefail

if [[ $# -ne 2 ]]; then
  echo "usage: $0 <transcript.jsonl> <slug>" >&2
  exit 2
fi

TRANSCRIPT="$1"
SLUG="$2"
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
MECH_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
NOTES="$MECH_DIR/instances/${SLUG}-inspector-notes.md"

[[ -f "$TRANSCRIPT" ]] || { echo "transcript not found: $TRANSCRIPT" >&2; exit 2; }
command -v claude >/dev/null 2>&1 || { echo "claude CLI not found in PATH" >&2; exit 2; }

# Extract user-text + assistant-text + tool_use shape into a compact stream
# the inspector can read in one pass. Strip the long content blobs.
COMPACT=$(jq -r '
  if (.message.role // "") == "user" then
    "USER: " + (.message.content | if type == "string" then . else (map(select(.type=="text").text // "")) | join(" ") end | .[0:500])
  elif (.message.role // "") == "assistant" then
    if (.message.content | type) == "array" then
      .message.content[] |
      if .type == "text" then "ASSISTANT-TEXT: " + (.text // "" | .[0:500])
      elif .type == "tool_use" then "TOOL: " + (.name // "?") + " " + ((.input.file_path // .input.path // .input.command // "") | .[0:200])
      else empty end
    else
      "ASSISTANT: " + (.message.content | tostring | .[0:500])
    end
  else empty end
' "$TRANSCRIPT" 2>/dev/null | head -300)

PROMPT="You are an Inspector for a **mechanical-test Actor transcript**. Important: this is a subagent dispatched by an orchestrator (the only USER turn is the initial dispatch). The Actor plays BOTH the student-pasting-prompts AND Claude-responding roles inside its own assistant turns. Most turns are ASSISTANT-TEXT or TOOL.

Your job: spot **sequence and coherence problems**, NOT content quality, NOT the Actor's role-switching pattern.

Specifically look for:
- Did the Actor **conflate two prompts** into one execution (e.g., respond to prompt-002 with prompt-003's content, or skip a phase entirely)?
- Did the Actor **lose the thread** mid-chain (revert to earlier phase framing, redo a phase, write the same file twice with different content)?
- Did the Actor **get stuck** looping / apologizing / re-trying the same tool call?
- Did the Actor **paraphrase a prompt** instead of Reading and pasting it verbatim (V-check failure shape)?
- Did tool calls go to **wrong paths** (writing to module-3 when the runner says module-4, or to a path the runner forbade)?

Do NOT flag:
- Reading two prompts back-to-back inside one assistant turn before responding to either — that IS the Actor pattern.
- Fabricated USER-style content (e.g., \"Maija answers:\") — the runner instructs the Actor to substitute.
- Brief / stub content — the Actor is told to stub.
- Content quality, framing scope, or file insightfulness.

Output: ≤200 words.
- Quote at most 3 specific turns where you saw an issue (use the USER/ASSISTANT-TEXT prefix and a short snippet).
- If clean, say so in 1-2 lines.
- End with **EXACTLY ONE** of: \`Verdict: PASS-WITH-NOTES\` or \`Verdict: REVIEW-NEEDED\`.

Compact transcript:

$COMPACT"

# Dispatch via claude --print with haiku
if ! OUTPUT=$(echo "$PROMPT" | claude --print --model haiku --no-session-persistence 2>&1); then
  echo "claude dispatch failed: $OUTPUT" >&2
  exit 2
fi

{
  echo "# Inspector notes — $SLUG"
  echo
  echo "Generated by bin/inspector.sh against $(basename "$TRANSCRIPT") on $(date '+%Y-%m-%d %H:%M:%S')."
  echo "Advisory only — not a PASS/FAIL gate. The script judge owns the verdict."
  echo
  echo "$OUTPUT"
} > "$NOTES"

verdict=$(echo "$OUTPUT" | grep -oE '(Verdict:|Verdict\*\*:|\*\*)?\s*(PASS-WITH-NOTES|REVIEW-NEEDED)' | tail -1)
echo "${verdict:-Verdict: UNCLEAR} → $NOTES"
exit 0
