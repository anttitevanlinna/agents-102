    destroy() {
        this.banners.forEach(banner => {
            banner.particles.forEach(el => {
                if (el.parentNode) el.parentNode.removeChild(el);
            });
            if (banner.container.parentNode) {
                banner.container.parentNode.removeChild(banner.container);
            }
        });
        this.banners = [];
    }
